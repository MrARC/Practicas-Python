"""
Los enteros también pueden usarse como condicionales
0 = False
1 = True
"""

# Equivale a true, porque 0 es igual a False
print(0 == False)

# Equivale a true, porque 1 es igual a True
print(1 == True)

# Equivale a false, porque 1 no es igual a False
print(1 == False)

# Equivale a True
# 4 no equivale a True (verdadero) y ese valor no equivale a False (verdadero)
# por lo tanto es verdadera la sentencia
print(4 != True != False)

# Equivale a true
print (1 == 1)

# True también
print (0 == 0)

# Falso
print (1 != 1)

# True
print (1 == 1 and 0 == False)

# True
print (1 != False and 0 != True)