# Sumatoria
a = 4+4
print('4 + 4 =', a)

# Resta
b = a - 3
print (a, '- 3 =', b)

# Multiplicaición
c = a*3
print (a, 'x 3 =', c)

# División
d = c / 2
print(c, '/ 2 =', d)

# Truncada
f = 8 // 3
print ('División (truncada a entero) de 8/3 =', f)

# Modulo
g = f % a
print ('Modulo de', f, '%', a,'=',g)

# Potencia
b3 = b**3
print ('b(', b, ') elevado a la tercia =', b3)